# Decide what tool to use to download data
DNLD_PROG=""
if command -v wget >/dev/null 2>&1; then
   PROG=wget
   OPTIONS="--tries=1 --timeout=3 -nv -q -O"
   if [ $VERBOSE -eq 1 ]; then
      OPTIONS="--tries=1 --timeout=3 -nv -O"
   fi
